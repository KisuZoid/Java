package Java.JAVA_DSA; //Package Declaration --> it indicates the folder structure where this JAVA file is located

public class _001_Intro { //class _001_Intro is public, i.e. accessible from other classes
    public static void main(String[] args) { //main method, entry point of the program
        System.out.println("Hello World"); //Prints "Hello World" to the console
    }
}

//single line comment
/*
 * Multi
 * Line
 * comment
 */

 
/*
 * public: 
 *      it is the access modifier that allows the class to be accessible from other classes.
 * 
 * static: 
 *      it allows the main method/function to be called without creating an instance/object of the class _001_Intro.
 * 
 * void: 
 *      it indicates that the main method does not return any value.
 * 
 * main:
 *      it is the name of the method that is the entry point of the program.
 * 
 * String[] args:
 *      it is an array of strings that can be passed as command-line arguments.
 * 
 * main(String[] args):
 *      it means that the main method takes an array of strings as input. in simple words, it allows the program to accept input from the user at runtime.
 */
